package com.davis.bluefolder.jsonapi;

/**
 * The type Json api response error link.
 */
public class JsonApiResponseErrorLink {

	/**
	 * The Url.
	 */
	private String url;
	/**
	 * The Href.
	 */
	private String href;
	/**
	 * The Meta.
	 */
	private String meta;
	/**
	 * The Self.
	 */
	private String self;

	/**
	 * Gets url.
	 *
	 * @return the url
	 */
	public String getUrl() {
		return url;
	}

	/**
	 * Sets url.
	 *
	 * @param url the url
	 */
	public void setUrl(String url) {
		this.url = url;
	}

	/**
	 * Gets href.
	 *
	 * @return the href
	 */
	public String getHref() {
		return href;
	}

	/**
	 * Sets href.
	 *
	 * @param href the href
	 */
	public void setHref(String href) {
		this.href = href;
	}

	/**
	 * Gets meta.
	 *
	 * @return the meta
	 */
	public String getMeta() {
		return meta;
	}

	/**
	 * Sets meta.
	 *
	 * @param meta the meta
	 */
	public void setMeta(String meta) {
		this.meta = meta;
	}

	/**
	 * Gets self.
	 *
	 * @return the self
	 */
	public String getSelf() {
		return self;
	}

	/**
	 * Sets self.
	 *
	 * @param self the self
	 */
	public void setSelf(String self) {
		this.self = self;
	}
}
